object main extends App {
  // vals are like constants. Immutables
  val x: Int = 42

  val aString: String = "Hello"

  // Notice characters are declared with single strings
  val aChar: Char = 'a'

  val aBoolean: Boolean = true

  // Shorts are 16 bits integers
  val aShort: Short = 4321

  // Longs are 64 bits integers
  val aLong: Long = 5436748758436354

  // 32 bits, single precision float
  val aFloat: Float = 2.03f

  // 64 bits, double precision float
  val aDouble: Double = 3.14

}
